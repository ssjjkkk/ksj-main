package kr.or.dw.ranking.vo;

public class GameVO {
	
	private int g_no;
	private int g_like;
	private int g_user;
	private String g_name;
	private String g_img;
	private String g_url;
	private String g_com;
	public int getG_no() {
		return g_no;
	}
	public void setG_no(int g_no) {
		this.g_no = g_no;
	}
	public int getG_like() {
		return g_like;
	}
	public void setG_like(int g_like) {
		this.g_like = g_like;
	}
	public int getG_user() {
		return g_user;
	}
	public void setG_user(int g_user) {
		this.g_user = g_user;
	}
	public String getG_name() {
		return g_name;
	}
	public void setG_name(String g_name) {
		this.g_name = g_name;
	}
	public String getG_img() {
		return g_img;
	}
	public void setG_img(String g_img) {
		this.g_img = g_img;
	}
	public String getG_url() {
		return g_url;
	}
	public void setG_url(String g_url) {
		this.g_url = g_url;
	}
	public String getG_com() {
		return g_com;
	}
	public void setG_com(String g_com) {
		this.g_com = g_com;
	}

}
